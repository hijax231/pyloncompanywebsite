<div class="pm1"style="color: #777;background-color:white;text-align:center;text-align: justify;">

	<p><b>PYLON INTERNATIONAL TRADING</b> was founded in February 02, 1990 as a single proprietor company
		with the purpose of importation, marketing and distribution of electro – mechanical components such as
		switches, relays, synchronous motors, capacitors, contactors, POS keyboard, telephone, communication
		equipment, press and hydraulic machinery, petrol products (gasoline hoses, nozzles, gasoline and water
		finding pastes) office equipment and supplies including novelty items and corporate give-away.</p>


	<p>PYLON was organized to focus in developing and sourcing of alternative products that could be of
		help to the growth of the export segment in the Philippine industry.
	</p>

	<p>PYLON as its main line is, supplying electronic components and parts to big appliance manufacturers in
		the Philippines. Through the years, the company expanded and that on August 15, 1997, PYLON
		INTERNATIONAL TRADING CORPORATION (PITC) was incorporated.</p>


	<p>On March 2000, PITC ventured into Information Technology with the purpose of providing local
		government units (LGU’s) of integrated IT-based solutions. For this, we partnered with a leading IT
		development company, for the purpose of implementing our vision
	</p>

	<p>In late 2003, PITC decided to venture on its own IT solution and invested to develop an eLearning
		solution for Grade School and High School level at the same time a One-Stop solution to be used by
		Local Government units to handle Revenue Tax Collections. In the middle of 2006, PITC was able to
		sign two (2) contracts, one for the eLearning solution and the other one for LGU Tax Collection solution.
		Both projects are now being used.
	</p>


	<p>PITC at present has deployed twenty five (25) elearning laboratories, thirty four (34) classroom
		connectivity solutions, a Revenue Tax collection system, Hospital System and a Local Civil
		Registry System.

	</p>

	<p>PITC has a team of highly qualified IT software designers and consultants to support the installation of
		hardware and software for the Local Government Unit’s (LGU’s) and private organizations. Among the
		services offered are as follows: Software Development, System Analysis, Database Build up, 3D Designs,
		Cabling and Networking.

	</p>
